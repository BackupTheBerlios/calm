<h2>Aim</h2>

CALM is aimed to create a license management system that solves the users problems (not the monetary ones of the software industry), so it has several targets:

<ul>
<li>show up unused licenses: they may be reused where needed
<li>built on web technology: every admin in the intranet is able to connect to the central license pool, no need for complicated papers
<li>built with OpenSource Software, so that it does not introduce potential license problems of its own
</ul>

<h2>Status</h2>

CALM is currently in a pre-alpha status. It will need some time to create a prototype that can be used outside.


